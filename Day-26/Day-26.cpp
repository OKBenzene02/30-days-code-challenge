///--- Vectors ---///
#include <bits/stdc++.h>

using namespace std;

void display(vector<int> &v)
{
    for (int i = 0; i < v.size(); i++)
    {
        cout<<v[i]<<endl;
    }
    cout<<endl;
}


int main(){
    vector<int> vec1;
    vector<int> vec2; // integer vector
    vector<char> vec3(4); // Character vector 
    vector<char> vec4(vec3); // 4 - element character vector made from vec3
    vector<int> v(6, 3); // 6-elements of 3's
    
    //using push_back and pop method...
    for (int i = 0; i < 4; i++)
    {
        int element;
        cout<<"Enter an element to store it in vector: "<<endl;
        cin>>element;
        vec1.push_back(element);
    }
    cout<<endl;
    vec1.pop_back(); // removes last element....
    cout<<endl;
    
    //insertion using iter...
    vector<int>:: iterator iter = vec1.begin();
    vec1.insert(iter+1, 234); // to insert one at a time
    vec1.insert(iter, 3, 234); // to insert the same element n times.
    
    
    display(vec1);
    return 0;
}
